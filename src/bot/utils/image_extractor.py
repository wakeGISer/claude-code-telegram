"""Validate image file paths and prepare them for Telegram delivery.

Used by the MCP ``send_image_to_user`` tool intercept — the stream callback
validates each path via :func:`validate_image_path` and collects
:class:`ImageAttachment` objects for later Telegram delivery.
"""

import re
from dataclasses import dataclass
from pathlib import Path
from typing import List, Optional

import structlog

logger = structlog.get_logger()

# Supported image extensions -> MIME types
IMAGE_EXTENSIONS = {
    ".png": "image/png",
    ".jpg": "image/jpeg",
    ".jpeg": "image/jpeg",
    ".gif": "image/gif",
    ".webp": "image/webp",
    ".bmp": "image/bmp",
    ".svg": "image/svg+xml",
}

# Raster formats that can be sent via reply_photo() (Telegram supports these natively)
TELEGRAM_PHOTO_EXTENSIONS = {".png", ".jpg", ".jpeg", ".gif", ".webp", ".bmp"}

# Safety caps
MAX_IMAGES_PER_RESPONSE = 10
MAX_FILE_SIZE_BYTES = 50 * 1024 * 1024  # 50 MB
PHOTO_SIZE_LIMIT = 10 * 1024 * 1024  # 10 MB — Telegram photo API limit


@dataclass
class ImageAttachment:
    """An image file to attach to a Telegram response."""

    path: Path
    mime_type: str
    original_reference: str


def validate_image_path(
    file_path: str,
    approved_directory: Path,
    caption: str = "",
) -> Optional[ImageAttachment]:
    """Validate a single image path from an MCP ``send_image_to_user`` call.

    Returns an :class:`ImageAttachment` if the path is a valid, existing image
    inside *approved_directory*, or ``None`` otherwise.
    """
    try:
        path = Path(file_path)
        if not path.is_absolute():
            return None

        resolved = path.resolve()

        # Security: must be within approved directory
        try:
            resolved.relative_to(approved_directory.resolve())
        except ValueError:
            logger.debug(
                "MCP image path outside approved directory",
                path=str(resolved),
                approved=str(approved_directory),
            )
            return None

        if not resolved.is_file():
            return None

        file_size = resolved.stat().st_size
        if file_size > MAX_FILE_SIZE_BYTES:
            logger.debug("MCP image file too large", path=str(resolved), size=file_size)
            return None

        ext = resolved.suffix.lower()
        mime_type = IMAGE_EXTENSIONS.get(ext)
        if not mime_type:
            return None

        return ImageAttachment(
            path=resolved,
            mime_type=mime_type,
            original_reference=caption or file_path,
        )
    except (OSError, ValueError) as e:
        logger.debug("MCP image path validation failed", path=file_path, error=str(e))
        return None


def extract_images_from_text(
    text: str,
    approved_directory: Path,
) -> List[ImageAttachment]:
    """Extract image file paths mentioned in Claude's response text.

    Looks for absolute paths ending in image extensions. This catches
    images generated by CLI-mode tools (Write/Bash) that don't go
    through MCP send_image_to_user.
    """
    if not text:
        return []

    extensions_pattern = "|".join(
        re.escape(ext) for ext in IMAGE_EXTENSIONS
    )
    # Match absolute paths like /Users/foo/bar.png
    pattern = rf"(/[^\s\"'`<>]+(?:{extensions_pattern}))"
    matches = re.findall(pattern, text, re.IGNORECASE)

    images: List[ImageAttachment] = []
    seen: set = set()
    for match in matches:
        if match in seen:
            continue
        seen.add(match)
        img = validate_image_path(match, approved_directory)
        if img and len(images) < MAX_IMAGES_PER_RESPONSE:
            images.append(img)

    return images


def strip_image_paths(text: str, images: List[ImageAttachment]) -> str:
    """Remove extracted image file paths from response text.

    Strips lines that contain only an image path (with optional surrounding
    whitespace/punctuation), so users see clean text without raw paths.
    """
    if not text or not images:
        return text

    path_strings = {str(img.path) for img in images}
    # Also match the original reference (pre-resolve path)
    path_strings.update(img.original_reference for img in images)

    cleaned_lines: List[str] = []
    for line in text.split("\n"):
        stripped = line.strip()
        # Skip lines that are just a file path (possibly with punctuation wrapper)
        # e.g. "/Users/foo/bar.png" or "- /Users/foo/bar.png" or "`/path/to/img.png`"
        bare = stripped.lstrip("-*•> ").rstrip(" .,;:").strip("`\"'")
        if bare in path_strings:
            continue
        # Also remove inline path references from within text
        modified = line
        for p in path_strings:
            modified = modified.replace(p, "")
        # Keep line if it still has meaningful content after stripping
        if modified.strip():
            cleaned_lines.append(modified)
        elif not stripped:
            # Preserve blank lines for formatting
            cleaned_lines.append("")

    # Remove trailing blank lines
    while cleaned_lines and not cleaned_lines[-1].strip():
        cleaned_lines.pop()

    return "\n".join(cleaned_lines)


def should_send_as_photo(path: Path) -> bool:
    """Return True if the image should be sent via reply_photo().

    Raster images ≤ 10 MB are sent as photos (inline preview).
    SVGs and large files are sent as documents.
    """
    ext = path.suffix.lower()
    if ext not in TELEGRAM_PHOTO_EXTENSIONS:
        return False

    try:
        return path.stat().st_size <= PHOTO_SIZE_LIMIT
    except OSError:
        return False
